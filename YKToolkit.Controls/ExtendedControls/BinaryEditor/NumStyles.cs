namespace YKToolkit.Controls
{
    /// <summary>
    /// 数値表現の列挙体を表します。
    /// </summary>
    public enum NumStyles
    {
        /// <summary>
        /// 符号なし 10 進数表現を表します。
        /// </summary>
        Decimal,

        /// <summary>
        /// 符号あり 10 進数表現を表します。
        /// </summary>
        SignedDecimal,

        /// <summary>
        /// 16 進数表現を表します。
        /// </summary>
        Hexadecimal,
    }
}
